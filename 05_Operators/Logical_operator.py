a = True
b =  False

# And operator check both the condition, if one is false the give false
print("and operator")
print(a and a)
print(a and b)
print(b and a)
print(b and b)

# OR operator also check both the condition, but if a single conditon got true it will give you a true
print("Or operator")
print(a or a)
print(a or b)
print(b or a)
print(b or b)

# NOT operator just reversed the answer, for ex. true changed in false, and false changed in True
print("Not operator")
print(not(a))
print(not(b))


